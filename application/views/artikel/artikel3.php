      <p>Pola hidup sehat sebaiknya dijalankan sejak dini agar terhindar dari berbagai serangan penyakit. 
        Penyakit yang mengintai akibat pola hidup yang buruk antara lain diabetes, gagal ginjal, kanker,
         maupun penyakit kardiovaskular seperti stroke, darah tinggi, dan jantung.Sebaliknya, apabila menerapkan 
         pola hidup sehat nantinya akan memberi efek yang luar biasa dan terhindar dari penyakit tersebut.</p>

      <p>Dengan memiliki kesehatan tubuh yang lebih baik, Anda dapat merasa lebih semangat dalam menjalani hidup
         serta meningkatkan produktivitas. Jangan tunda untuk mulai merencanakan dan menerapkan kebiasaan gaya hidup
          sehat.</p>

      <p>Meskipun sulit untuk keluar dari kebiasaan lama, terapkan gaya hidup sehat secara bertahap namun konsisten.
         Meski perlahan namun dampak yang dirasakan cukup signifikan, yakni dapat memberi lebih banyak energi pada tubuh, 
         meningkatkan kesehatan mental, dan menyingkirkan penyakit.</p>

         <p>Berikut lima langkah mudah menuju pola hidup sehat.</p>
          
    <blockquote>
      <p><strong>Mengapa bisa terjadi ?</strong></p>
      <p>Alergi makanan terjadi sebagai bentuk respon tubuh, dimana sistem kekebalan tubuh menganggap protein dalam makanan
         sebagai suatu ancaman bagi tubuh. Sehingga tubuh melepaskan senyawa kimia yang memicu reaksi alergi. Reaksi alergi
        biasanya muncul dalam beberapa menit sampai beberapa jam setelah mengonsumsi makanan alergenik. Mereka yang sangat 
        sensitif dapat mengalami reaksi alergi meskipun jumlah makanan penyebab yang dikonsumsi sangat sedikit. Reaksi alergi 
        yang timbulpun bisa ringan namun pada mereka yang sangat sensitif terhadap makanan tertentu misalnya kacang dampaknya 
        bisa fatal hingga menyebabkan kematian. Oleh sebab itu, sangat penting bagi kita untuk mengenali gejala, pengobatan
        dan pencegahannya.</p>
    </blockquote>
    <blockquote>
    <p><strong>Faktor risiko alergi makanan</strong></p>
    <p>Alergi makanan lebih berisiko dialami oleh orang yang menderita alergi lain, seperti rhinitis alergi atau asma. Orang 
      yang sudah terkena alergi pada satu jenis makanan juga lebih rentan menderita alergi pada jenis makanan lain. Faktor lain
       yang dapat meningkatkan risiko seseorang terserang alergi makanan adalah:
        <ul>
            <li>Berusia di bawah 5 tahun</li>
            <li>Memiliki keluarga yang menderita riwayat alergi, seperti biduran atau asma</li>
        </ul>
    </p>
    </blockquote>

    <blockquote>
    <p><strong>Gejala Alergi Makanan</strong></p>
    <p>Pada sebagian orang, alergi makanan dapat menyebabkan penderitanya merasa tidak nyaman, meski tidak terlalu parah. 
      Gejala sering kali muncul dalam beberapa menit sampai 2 jam setelah mengonsumsi makanan pemicu alergi.
      Gejala alergi makanan yang muncul sama dengan reaksi alergi pada umumnya, yaitu:
      <ul>
            <li>Pilek atau hidung tersumbat</li>
            <li> Ruam kulit yang terasa gatal </li>
            <li>Gatal di mulut, tenggorokan, mata, dan di bagian tubuh lain</li>
            <li>Pembengkakan pada wajah, bibir, lidah, atau tenggorokan (angioedema) </li>
            <li>Sulit menelan dan berbicaran</li>
            <li>Mengi atau bengek </li>
            <li>Sesak napas</li>

        </ul>
    </p>
    </blockquote>

    <blockquote>
    <p><strong>Pengobatan Alergi Makanan</strong></p>
    <p>Cara terbaik untuk mengatasi alergi makanan adalah dengan menghindari makanan penyebab alergi. Meski demikian, seseorang 
      mungkin saja mengonsumsi makanan tersebut secara tidak sengaja. Bila hal ini terjadi, ada beberapa obat yang bisa digunakan
      untuk meredakan gejala. Jika gejala yang muncul tergolong ringan, penderita bisa menggunakan antihistamin yang dijual bebas.
      Bila gejala masih terasa, penderita bisa ke dokter agar diberikan antihistamin dengan dosis </p>

    <p>Bila muncul gejala anafilaksis, penderita harus dibawa ke IGD rumah sakit untuk diberikan suntikan epinephrine. Setelah gejala
       hilang, dokter akan meminta pasien untuk selalu membawa suntikan tersebut.Penting untuk memahami cara menggunakan suntik epinephrine
       bila gejala alergi makanan yang Anda alami cukup parah. Selain itu, ajari juga orang-orang yang sering berada di dekat Anda, misalnya
       keluarga atau rekan kerja, mengenai cara menggunakan suntikan tersebut bila Anda terserang anafilaksis.</p>
    </blockquote>

